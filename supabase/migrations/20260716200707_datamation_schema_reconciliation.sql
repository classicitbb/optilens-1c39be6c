CREATE OR REPLACE VIEW public.catalog_live AS
  SELECT l.id, 'lens'::text AS product_type, l.id AS product_id, NULL::text AS sku, l.name,
    l.pricing_category AS category, l.supplier_id, s.name AS supplier_name, l.base_price AS cost,
    l.sell_price, 'USD'::text AS currency, l.show_on_website AS web_enabled,
    l.show_in_ws_pricelist AS wspl_enabled, l.is_active, l.created_at, l.updated_at,
    lt.name AS lenstype, m.name AS material, mf.name AS mftype
  FROM public.lenses l LEFT JOIN public.suppliers s ON s.id = l.supplier_id
    LEFT JOIN public.lenstypes lt ON lt.id = l.lenstype_id
    LEFT JOIN public.materials m ON m.id = l.material_id
    LEFT JOIN public.mftypes mf ON mf.id = l.mftype_id WHERE l.is_active
  UNION ALL
  SELECT sp.id, 'supply'::text, sp.id, sp.sku, sp.name, sp.category, sp.supplier_id, s.name,
    sp.base_price, sp.sell_price, COALESCE(sp.currency, 'USD'), sp.show_on_website, sp.stk_wspl,
    sp.is_active, sp.created_at, sp.updated_at, NULL::text, NULL::text, NULL::text
  FROM public.supplies sp LEFT JOIN public.suppliers s ON s.id = sp.supplier_id WHERE sp.is_active
  UNION ALL
  SELECT a.id, 'addon'::text, a.id, a.sku, a.name, a.category, a.supplier_id, s.name,
    a.cost, a.price, 'USD'::text, a.show_on_website, false, a.is_active, a.created_at, a.updated_at,
    NULL::text, NULL::text, NULL::text
  FROM public.addons a LEFT JOIN public.suppliers s ON s.id = a.supplier_id WHERE a.is_active;
REVOKE ALL ON public.catalog_live FROM anon, authenticated, PUBLIC;
GRANT SELECT ON public.catalog_live TO service_role;

CREATE OR REPLACE VIEW public.balances_public WITH (security_invoker = true) AS
  SELECT customer_id, account_number, credit_limit, current_balance, last_statement_amount,
    last_statement_date, last_payment_amount, last_payment_date, synced_at FROM public.balances;
CREATE OR REPLACE VIEW public.statements_public WITH (security_invoker = true) AS
  SELECT id::text AS id, customer_id, account_number, from_date AS period_start, to_date AS period_end,
    opening_balance, closing_balance, payments, finance_charges, discount, due_date, status, void, printed,
    innovations_emailed, portal_emailed_at, synced_at FROM public.statements;
CREATE OR REPLACE VIEW public.statement_lines_public WITH (security_invoker = true) AS
  SELECT sl.id, sl.innovations_statement_id::text AS statement_id, s.customer_id, s.account_number,
    sl.order_type, sl.invoice_id, sl.reference, sl.patient, sl.post_date, sl.amount
  FROM public.statement_lines sl JOIN public.statements s ON s.innovations_statement_id = sl.innovations_statement_id;
CREATE OR REPLACE VIEW public.customer_account_number_duplicates WITH (security_invoker = true) AS
  SELECT account_number, count(*) AS duplicate_count, array_agg(id ORDER BY id) AS customer_ids,
    array_agg(name ORDER BY id) AS customer_names
  FROM public.customers WHERE account_number IS NOT NULL AND btrim(account_number) <> ''
  GROUP BY account_number HAVING count(*) > 1;
CREATE OR REPLACE VIEW public.customer_payment_profile_public WITH (security_invoker = true) AS
  SELECT id AS customer_id, account_number, name, pay_by_card, pay_by_eft, eft_institution_name, default_payment_type
  FROM public.customers;
CREATE OR REPLACE VIEW public.customer_order_health WITH (security_invoker = true) AS
  SELECT c.id AS contact_id, c.name, c.pipeline, c.stage, oa.last_order_date,
    current_date - oa.last_order_date AS quiet_days, oa.avg_gap_days, oa.orders_last_30_days,
    CASE WHEN oa.last_order_date IS NULL THEN 'unknown'
      WHEN current_date - oa.last_order_date >= 21 THEN 'alarm'
      WHEN current_date - oa.last_order_date >= 14 THEN 'flag' ELSE 'ok' END AS health
  FROM public.contacts c JOIN public.order_activity oa ON oa.contact_id = c.id WHERE c.stage = 'customer';
CREATE OR REPLACE VIEW public.store_product_variants_public WITH (security_invoker = true) AS
  SELECT id, product_type, product_id, title, variant_key, sku, opc_code, attribute_values AS attributes,
    metadata, price, stock_qty, 0::integer AS reserved_qty, low_stock_threshold, allow_backorder,
    is_active, sort_order, created_at, updated_at FROM public.product_variants WHERE is_active = true;
REVOKE ALL ON public.balances_public, public.statements_public, public.statement_lines_public,
  public.customer_account_number_duplicates, public.customer_payment_profile_public FROM anon, PUBLIC;
GRANT SELECT ON public.balances_public, public.statements_public, public.statement_lines_public,
  public.customer_account_number_duplicates, public.customer_payment_profile_public TO authenticated, service_role;
GRANT SELECT ON public.store_product_variants_public TO anon, authenticated;

