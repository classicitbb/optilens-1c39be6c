    'customers', 'balances', 'bank_payment_portals', 'statements', 'statement_lines',
    'api_keys', 'api_audit_log', 'live_data_gateway_agents', 'live_data_gateway_requests',
    'innovations_sync_runs', 'innovations_sync_dead_letters', 'crm_pipelines', 'cadences',
    'cadence_steps', 'cadence_enrollments', 'order_activity', 'outreach_outbox',
    'product_variants', 'store_product_media', 'store_product_overrides'
  ]
  LOOP
    EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY', table_name);
  END LOOP;
END $$;

