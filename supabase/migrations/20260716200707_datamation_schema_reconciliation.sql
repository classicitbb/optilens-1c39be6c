    'catalog_templates', 'catalog_sections', 'catalog_assignments', 'catalog_pages',
    'catalog_page_objects', 'material_upgrades', 'matrix_allocations', 'price_matrix',
    'pricelist_catalog_rows', 'pricelist_child_sections', 'pricelist_line_overrides', 'pricelist_notes',
